const unsigned char * font_lookup(char c) {
    if (c >= '0' && c <= '9') {
        return &FONT_DATA[(c - '0' + 2) * 6];
    } else if (c >= 'A' && c <= 'F') {
        return &FONT_DATA[(c - 'A' + 12) * 6];
    } else if (c == ' ') {
        return &FONT_DATA[0];
    } else if (c >= '!' && c <= '/') {
        return &FONT_DATA[(c - '!' + 38) * 6];
    } else if (c >= ':' && c <= '@') {
        return &FONT_DATA[(c - ':' + 53) * 6];
    } else if (c >= '[' && c <= '`') {
        return &FONT_DATA[(c - '[' + 60) * 6];
    } else if (c >= '{' && c <= '~') {
        return &FONT_DATA[(c - '{' + 66) * 6];
    } else {
        return &FONT_DATA[6];
    }
}

